package Inventaris;

public class Kebersihan_Kelas {
    private String Sirkulasi_Udara;
    private int Nilai_Cahaya;
    private int Kelembaban;
    private int suhu;

    public String getSirkulasi_Udara() {
        return Sirkulasi_Udara;
    }

    public void setSirkulasi_Udara(String Sirkulasi_Udara) {
        this.Sirkulasi_Udara = Sirkulasi_Udara;
    }

    public int getNilai_Cahaya() {
        return Nilai_Cahaya;
    }

    public void setNilai_Cahaya(int Nilai_Cahaya) {
        this.Nilai_Cahaya = Nilai_Cahaya;
    }

    public int getKelembaban() {
        return Kelembaban;
    }

    public void setKelembaban(int Kelembaban) {
        this.Kelembaban = Kelembaban;
    }

    public int getSuhu() {
        return suhu;
    }

    public void setSuhu(int suhu) {
        this.suhu = suhu;
    }
}
